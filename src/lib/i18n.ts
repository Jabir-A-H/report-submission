export type Translations = typeof translations.bn;

/**
 * Utility for Server Components to get translations based on the NEXT_LOCALE cookie.
 */
export async function getServerLanguage() {
  const { cookies } = await import("next/headers");
  const cookieStore = await cookies();
  const lang = (cookieStore.get("NEXT_LOCALE")?.value as Language) || "bn";
  const safeLang = translations[lang] ? lang : "bn";
  const t = translations[safeLang];
  
  const tc = (category: string) => {
    return t.categories[category as keyof typeof t.categories] || category;
  };

  return { language: safeLang, t, tc };
}
